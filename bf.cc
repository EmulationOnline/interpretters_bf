void run(const char* code) {
    uint8_t cells[30000];
    memset(cells, 0, 30000);
    int cell_ptr = 0;
    int code_ptr = 0;
    const int code_len = strlen(code);
    std::unordered_map<size_t, size_t> jumps;
    std::vector<int> stack;
    // precompute jumps
    for (int i = 0; i < code_len; i++) {
        if (code[i] == '[') {
            stack.push_back(i);
        } else if (code[i] == ']') {
            if (stack.size() == 0) {
                puts("Empty stack!");
                exit(1);
            }
            int open = stack.back(); stack.pop_back();
            jumps[open] = i;
            jumps[i] = open;
        }
    }
    if (stack.size() != 0) {
        puts("unmatched jumps in program.");
        exit(1);
    }

    while (code_ptr >= 0 && code_ptr < code_len) {
        char c = code[code_ptr];
        switch (c) {
            case '+':
                cells[cell_ptr] ++;
                break;
            case '-':
                cells[cell_ptr] --;
                break;
            case '<':
                cell_ptr --;
                break;
            case '>':
                cell_ptr ++;
                break;
            case '[':
                if (cells[cell_ptr] == 0) code_ptr = jumps[code_ptr];
                break;
            case ']':
                if (cells[cell_ptr] != 0) code_ptr = jumps[code_ptr];
                break;
            case '.':
                write(STDOUT_FILENO, &cells[cell_ptr], 1);
                break;
        }
        code_ptr ++;
    }

}

