const std = @import("std");
const Io = std.Io;

fn run(io: Io, code: []const u8) !void {
    _ = io;
    var tape: [30000]u8 = undefined;
    var cell_ptr: u16 = 0;
    var code_ptr: u16 = 0;
    const code_len = code.len;
    @memset(tape[0..], 0);

    // calculate jumps
    var buffer: [100000]u8 = undefined;
    var fba: std.heap.FixedBufferAllocator = .init(&buffer);
    var stack: std.ArrayList(u16) = .empty;
    var open: std.AutoHashMap(u16, u16) = .init(fba.allocator());
    var close: std.AutoHashMap(u16, u16) = .init(fba.allocator());
    for (code, 0..) |ch, i| {
        if (ch == '[') try stack.append(fba.allocator(), @intCast(i));
        if (ch == ']') {
            const left = stack.pop() orelse @panic("mismatch");
            const right = i;
            try open.put(@intCast(right), left);
            try close.put(left, @intCast(right));
        }
    }
    if (stack.items.len != 0) {
        @panic("unclosed [");
    }

    while (code_ptr < code_len) {
        const c = code[code_ptr];
        switch (c) {
            '<' => {
                if (cell_ptr == 0) {
                    @panic("tape underflow");
                }
                cell_ptr -= 1;
            },
            '>' => {
                cell_ptr += 1;
                if (cell_ptr >= code_len) {
                    @panic("tape overflow");
                }
            },
            '+' => {
                tape[cell_ptr] +%= 1;
            },
            '-' => {
                tape[cell_ptr] -%= 1;
            },
            '[' => {
                if (tape[cell_ptr] == 0) {
                    code_ptr = close.get(code_ptr) orelse @panic("bad jump");
                }
            },
            ']' => {
                if (tape[cell_ptr] != 0) {
                    code_ptr = open.get(code_ptr) orelse @panic("bad jump");
                }
            },
            '.' => {
                const ch = tape[cell_ptr];
                std.debug.print("{c}", .{ch});
            },
            else => {},
        }
        code_ptr += 1;
    }

    std.debug.print("done", .{});
}
