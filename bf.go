import "os"

type Jumps = map[int]int

func gen_jumps(program string) (Jumps, Jumps) {
    // returns starts (end->start) and stops(start->end)
    starts := make(Jumps)
    stops := make(Jumps)
    var stack []int = nil
    for i, char := range program {
        if (char == '[') {
            stack = append(stack, i)
        } else if (char == ']') {
            right := i
            last := len(stack)-1
            left := stack[last]
            stack = stack[:last]
            stops[left] = right
            starts[right] = left
        }
    }
    return starts, stops
}

func run(program string) {
    starts, stops := gen_jumps(program)

    var pc = 0
    var tape [30000]uint8
    var ptr = 0
    // var output string

    for pc < len(program) {
        switch program[pc] {
            case '+':
                tape[ptr] += 1
            case '-':
                tape[ptr] -= 1
            case '.':
                var v = tape[ptr]
                // var ch = rune(tape[ptr])
                // fmt.Printf("%c", tape[ptr])
                os.Stdout.Write([]byte{v})
                // output += string(ch)
            case '<':
                if (ptr == 0) {
                    panic("ptr underflow")
                }
                ptr -= 1
            case '>':
                if (ptr == len(tape) -1) {
                    panic("ptr overflow")
                }
                ptr += 1
            case '[':
                if tape[ptr] == 0 {
                    pc = stops[pc]
                }
            case ']':
                if tape[ptr] != 0 {
                    pc = starts[pc]
                }
        }
        pc += 1
    }

    print("done")
    // return output
}

